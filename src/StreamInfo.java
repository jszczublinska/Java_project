    private void jList1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MousePressed
    String selectedText = jList1.getSelectedValue();

    if (selectedText != null) {
        for( Stream stream : simulation.getStreamList()){
            String currStreamName = stream.getName();
            if (currStreamName.equals(selectedText)) {
                jTextArea1.setText(stream.InformationToString());
                jLabel1.setIcon(stream.getThumbnail());
                break;
            }
        }
    }    }//GEN-LAST:event_jList1MousePressed

